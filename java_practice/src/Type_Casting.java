
public class Type_Casting {

	public static void main(String[] args) {
		int num=10;
		System.out.println("Value of num="+num);
		double d=num;
		System.out.println("Value of d="+d);
		
		int a=(int)d;
		System.out.println("Value of a="+a);
		
		int val=150;
		byte bt=(byte)val;
		System.out.println(bt);
	}

}
